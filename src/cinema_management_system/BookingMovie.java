    public void Store() {
        String title = jTextField1.getText();
        int numoftickets = Integer.parseInt(jTextField2.getText());
        String cardNumber = jTextField3.getText();
        String expirationDate = jTextField4.getText();
        String cvv = jTextField5.getText();
        
        if (title.isEmpty() || numoftickets==0 || cardNumber.isEmpty() || expirationDate.isEmpty() || cvv.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All Fields Should be Filled", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (findMovie(title) && findCustomer(customer,username)) {             
                Booking book = new Booking();
                book.Add(title, numoftickets, username);
                bookings.add(book);
                
                JOptionPane.showMessageDialog(this, "Booking Confirmed", "Success", JOptionPane.INFORMATION_MESSAGE);
                CustomerMenu customerMenu = new CustomerMenu(username);  
                customerMenu.setVisible(true); 
                this.setVisible(false);  
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Movie not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    public boolean findCustomer(List<CustomerSignUp> customers, String username) {
        for (CustomerSignUp customer : customers) {
            if (customer.username.equalsIgnoreCase(username)) {
                return true;
            }
        }
        return false;
    }
    
    public boolean findMovie(String title) {
        for (AddMovie movie : movies) {
            if (movie.title.equalsIgnoreCase(title)) {
                return true;
            }
        }
        return false;
    }

