    public static boolean findCustomer(List<CustomerSignUp> customers, String username, String password) {
        for (CustomerSignUp customer : customers) {
            if (customer.username.equalsIgnoreCase(username) && customer.password.equalsIgnoreCase(password)) {
                return true;
            }
        }
        return false;
    }

