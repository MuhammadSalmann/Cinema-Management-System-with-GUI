private void displayBookings() {    
    List<Booking> bookings = BookingMovie.bookings; // Access the bookings list directly
    DefaultListModel<String> model = new DefaultListModel<>();
    for (Booking booking : bookings) {
        if(username.equalsIgnoreCase(booking.username)) {
            model.addElement(booking.toString()); // Convert booking to its string representation   
        }
    }
    jList1.setModel(model);
}


    
